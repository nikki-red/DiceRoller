class MainActivity : ComponentActivity() {
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContent {
            DiceRollerTheme {
                // A surface container using the 'background' color from the theme
                Surface(
                    modifier = Modifier.fillMaxSize(),
                    color = MaterialTheme.colorScheme.background
                ) {
                    DiceRollerApp()
                }
            }
        }
    }
}

@Composable
/*
The wrapContentSize() method specifies that the available space
should at least be as large as the components inside of it.
However, because the fillMaxSize() method is used,
if the components inside of the layout are smaller than the available space,
an Alignment object can be passed to wrapContentSize() method
that specifies how the components should align within the available space.
 */
fun DiceWithButtonAndImage(modifier: Modifier = Modifier) {
    /*
    mutableStateOf() returns an observabe
    -> when the value of result variable changes,
    it recomposes, value of result is reflected, UI refreshes
     */
    var result by remember { mutableStateOf( 1) }
    val imageResource = when (result) {
        1 -> R.drawable.dice_1
        2 -> R.drawable.dice_2
        3 -> R.drawable.dice_3
        4 -> R.drawable.dice_4
        5 -> R.drawable.dice_5
        else -> R.drawable.dice_6
    }
    Column(
        modifier = modifier,
        horizontalAlignment = Alignment.CenterHorizontally
    ) {
        Image(
            painter = painterResource(imageResource),
            contentDescription = result.toString()
        )
        //
        Spacer(modifier = Modifier.height(16.dp))
        Button(onClick = { result = (1..6).random() }) {
            Text(stringResource(R.string.roll))
        }
    }
}

@Preview(
    showBackground = true,
    showSystemUi = true
)
@Composable
fun DiceRollerApp() {
    DiceWithButtonAndImage(modifier = Modifier
        .fillMaxSize()
        .wrapContentSize(Alignment.Center)
    )
}

/*
Composables are stateless by default
-> Do not hold any value
-> Can be recomposed anytime by system, results in value being reset
This can be avoided by using 'remember' -> storing an object in the memory

 */